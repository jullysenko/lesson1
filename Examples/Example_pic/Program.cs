void PrintImage(int[,] image)
{
    for(int i = 0; i < image.GetLength(0); i++)  // GetLength(0) - получить кол-во строк
    {
        for(int j = 0; j < image.GetLength(1); j++) // GetLength(1) - получить кол-во столбцов
        { 
            //Console.Write($"{image[i,j]} ");
             // если текущий элемент  image ij элемент совпадает с 0, то искуственно печатаем пробел
            if (image[i,j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
    Console.WriteLine();
    }
}

void FillImage(int row, int col)    // в качестве аргумента позиция строки и пикселя, с которого начинаем закраску
{
    if(pic[row, col] == 0) // проверяем условие, если текущий пиксель равен 0, т.е не закрашен, то мы его красим
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);  // Филл имадж вызывает сам себя, правило, что идетза чем
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row , col + 1);
    }
}
